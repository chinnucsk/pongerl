draw_number(0, X, Y, _) ->
    cecho:attron(?ceCOLOR_PAIR(?RESULT_PAIR)),
    cecho:move(Y, X),
    cecho:addstr("      "),
    cecho:move(Y + 1, X),
    cecho:addstr("  "),
    cecho:move(Y + 1, X + 4),
    cecho:addstr("  "),
    cecho:move(Y + 2, X),
    cecho:addstr("  "),
    cecho:move(Y + 2, X + 4),
    cecho:addstr("  "),
    cecho:move(Y + 3, X),
    cecho:addstr("  "),
    cecho:move(Y +3 , X + 4),
    cecho:addstr("  "),
    cecho:move(Y + 4, X),
    cecho:addstr("      "),
    cecho:refresh(),
    cecho:attroff(?ceCOLOR_PAIR(?RESULT_PAIR));
