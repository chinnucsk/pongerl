draw_number(N, X, Y, 1) ->
    FirstDigit = N div 10,
    SecondDigit = N rem 10,
    draw_number(FirstDigit, X - ?NX - 1, Y, 1),
    draw_number(SecondDigit, X, Y, 1);
draw_number(N, X, Y, 2) ->
    FirstDigit = N div 10,
    SecondDigit = N rem 10,
    draw_number(FirstDigit, X, Y, 1),
    draw_number(SecondDigit, X + ?NX + 1, Y, 1).
    
